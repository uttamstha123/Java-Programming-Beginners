package AccessModifier0;

public class Access1 {

	// There are 4 modifiers
	// Default , Public , Private, Protected
	// Default can be access only within the package
	// public can be access within the Project
	// private can be access within the class
	// protected can be access only within the package
	// This is default
	String name0 = "Uttam";
	// if i want to access the variable name to another class of package..
	
	// this is public
	public String name1 = "Sugam";
	// this is private
	private String name2 = "Mijju";
	// this is protected
	protected String name3 = "Ajbin";
}
