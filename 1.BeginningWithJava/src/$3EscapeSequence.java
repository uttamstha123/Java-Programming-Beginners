
public class $3EscapeSequence {
	public static void main(String args[]) {
		// If i want to print :
		// This is "Java" Program
		// System.out.println("This is "Java" Program"); this shows an error 
		// Thats why we use escape sequences
		System.out.println("This is \"Java\" Program");
		// There are more like this
		System.out.println("This is \"\\t\" \t Tab ");
		System.out.println("This is \"\\n\" \n new Line ");
	}
}
