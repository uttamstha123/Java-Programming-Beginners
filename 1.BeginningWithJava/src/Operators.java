
public class Operators {

	public static void main(String[] args) {
		// Arithmetic and Assignment Operators
		// = , + , - , / , * , %
		int a = 5; // the value of a is initilize using assignment operators
		int b = 6;
		int result = a + b;
		result = a - b;
		result = a * b;
		result = a / b;
		result = a % b;
//		System.out.println(result);
		
		// Comparison operator
		// 1. == (is equal to)
		// 2. < (is less than)
		// 3. > (is greater than)
		// 4. <= (is less than or equal to)
		// 5. >=  (is less than or equal to)
		
		// Bitwise operator
		// 1. << (left shift)
		// 2. >> (right shift)
		
		// Logical Operator
		// 1. && (And)
		// 2. ||(Or)
		// 3. !(Not)
	}

}
